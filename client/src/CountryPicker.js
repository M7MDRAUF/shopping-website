const CountryPicker = ({ value, onChange }) => {
  const [searchTerm, setSearchTerm] = useState('');
  const [isOpen, setIsOpen] = useState(false);
  const [highlightedIndex, setHighlightedIndex] = useState(-1);
  const wrapperRef = useRef(null); // Ref for the entire picker
  const dropdownRef = useRef(null); // Ref for the dropdown

  // Auto-detect user's country based on geolocation
  useEffect(() => {
    if (!value) {
      fetch('https://ipapi.co/json/')
        .then((response) => response.json())
        .then((data) => {
          if (data.country_name) {
            onChange(data.country_name);
          }
        })
        .catch((error) => console.error('Error fetching geolocation:', error));
    }
  }, [value, onChange]);

  // Debounced search
  const [debouncedSearchTerm, setDebouncedSearchTerm] = useState(searchTerm);
  useEffect(() => {
    const handler = setTimeout(() => {
      setDebouncedSearchTerm(searchTerm);
    }, 300);
    return () => clearTimeout(handler);
  }, [searchTerm]);

  const filteredCountries = countries.filter((country) =>
    country.name.toLowerCase().includes(debouncedSearchTerm.toLowerCase())
  );

  const handleSelect = (country) => {
    onChange(country.name);
    setIsOpen(false);
    setSearchTerm('');
    setHighlightedIndex(-1);
  };

  // Highlight matching text
  const highlightMatch = (text, term) => {
    if (!term) return text;
    const regex = new RegExp(`(${term})`, 'gi');
    const parts = text.split(regex);
    return parts.map((part, index) =>
      part.toLowerCase() === term.toLowerCase() ? (
        <span key={index} className="highlight">{part}</span>
      ) : (
        part
      )
    );
  };

  // Keyboard navigation
  const handleKeyDown = (e) => {
    if (!isOpen) return;

    if (e.key === 'ArrowDown') {
      e.preventDefault();
      setHighlightedIndex((prev) =>
        prev < filteredCountries.length - 1 ? prev + 1 : prev
      );
    } else if (e.key === 'ArrowUp') {
      e.preventDefault();
      setHighlightedIndex((prev) => (prev > 0 ? prev - 1 : 0));
    } else if (e.key === 'Enter' && highlightedIndex >= 0) {
      e.preventDefault();
      handleSelect(filteredCountries[highlightedIndex]);
    } else if (e.key === 'Escape') {
      setIsOpen(false);
      setHighlightedIndex(-1);
    }
  };

  // Scroll highlighted item into view
  useEffect(() => {
    if (highlightedIndex >= 0 && dropdownRef.current) {
      const highlightedItem = dropdownRef.current.children[highlightedIndex + 1]; // +1 for search input
      highlightedItem?.scrollIntoView({ block: 'nearest' });
    }
  }, [highlightedIndex]);

  // Handle click outside to close dropdown
  useEffect(() => {
    const handleClickOutside = (event) => {
      if (wrapperRef.current && !wrapperRef.current.contains(event.target)) {
        setIsOpen(false);
        setHighlightedIndex(-1);
      }
    };
    document.addEventListener('mousedown', handleClickOutside);
    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, []);

  return (
    <div className="country-picker" ref={wrapperRef} onKeyDown={handleKeyDown}>
      <input
        type="text"
        value={value || ''}
        onChange={(e) => onChange(e.target.value)}
        onFocus={() => setIsOpen(true)}
        // Removed onBlur to handle closing with click outside
        placeholder="Select or type your country"
        className="country-input"
        aria-label="Select your country"
        autoComplete="off"
      />
      {isOpen && (
        <div className="country-dropdown" role="listbox" ref={dropdownRef}>
          <input
            type="text"
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            placeholder="Search country..."
            className="country-search"
            autoFocus
            aria-label="Search for a country"
          />
          <ul className="country-list">
            {filteredCountries.map((country, index) => (
              <li
                key={country.code}
                onClick={() => handleSelect(country)}
                className={`country-item ${index === highlightedIndex ? 'highlighted' : ''}`}
                role="option"
                aria-selected={index === highlightedIndex}
              >
                <Flag country={country.code} size={20} className="country-flag" />
                <span>{highlightMatch(country.name, debouncedSearchTerm)}</span>
              </li>
            ))}
          </ul>
        </div>
      )}
    </div>
  );
};

export default CountryPicker;